public class Statebean {

    /**
     * date : 2016-10-05
     * id : 30
     * state : 2.6,5.6,3,2.3,62,32
     * time : 04:26:00
     */

    private String date;
    private String id;
    private String state;
    private String time;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
